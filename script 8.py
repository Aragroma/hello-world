# String literals
# sequence of characters surrounded by single, double or triple quotes
strings = "This is Python"
character = "C"
multiline_str = """This is a multiline string
with more than one line code."""
unicode = u"\u00dcnic\u00f6de"
raw_str = r"raw \n string"

print(strings)
print(character)
print(multiline_str)
print(unicode)
print(raw_str)
